
package test;

/**
 * demonstrate order of operations, representation of pow, etc.
 * @author jbf
 */
public class MathExprDemo {
    public static void main( String[] args ) {
        System.out.println(3+4);
        System.out.println(3*4);
        System.out.println(3-4);
        System.out.println(3/4);
        System.out.println(3*4+2);
        System.out.println(Math.pow(3,4));
    }
}
